        "class Agent():\n",
        "  def __init__(self, state_dim, action_dim, save_dir):\n",
        "    self.state_dim = state_dim\n",
        "    self.action_dim = action_dim\n",
        "    self.save_dir = save_dir\n",
        "    self.device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')\n",
        "    self.memory = deque(maxlen=10000)\n",
        "    self.batch_size = 32\n",
        "    self.gamma = 0.9\n",
        "    self.loss_fn = nn.SmoothL1Loss()  # Huber loss\n",
        "    self.burnin = 1e4  # min experiences before training\n",
        "    self.learn_every = 3  # num of exp between updates to Q_online\n",
        "    self.sync_every = 1e4  # num of experiences updates between Q_t and Q_o\n",
        "\n",
        "    self.net = ArseNet(self.state_dim, self.action_dim).float()\n",
        "    self.net = self.net.to(self.device)\n",
        "\n",
        "    self.optimizer = torch.optim.Adam(self.net.parameters(), lr=.00025)\n",
        "\n",
        "    self.exploration_rate = 1\n",
        "    self.exploration_rate_decay = 0.9999995\n",
        "    self.exploration_rate_min = 0.1\n",
        "    self.curr_step = 0\n",
        "\n",
        "    self.save_every = 5e5\n",
        "\n",
        "  \n",
        "  def act(self, state):\n",
        "    '''\n",
        "    choose epsilon-greedy action given state and update the value of step\n",
        "    '''\n",
        "    # exploration\n",
        "    if np.random.rand() < self.exploration_rate:\n",
        "      action_idx = np.random.randint(self.action_dim)\n",
        "    # exploitation\n",
        "    else:\n",
        "      state = state.__array__()\n",
        "      state = torch.tensor(state).to(self.device)\n",
        "      state = state.unsqueeze(0)\n",
        "      action_values = self.net(state, model='online')\n",
        "      action_idx = torch.argmax(action_values, axis=1).item()\n",
        "    \n",
        "    # decrease exploration_rate\n",
        "    self.exploration_rate *= self.exploration_rate_decay\n",
        "    if self.exploration_rate < self.exploration_rate_min:\n",
        "      self.exploration_rate = self.exploration_rate_min\n",
        "    \n",
        "    self.curr_step += 1\n",
        "    return action_idx\n",
        "  \n",
        "  def cache(self, state, next_state, action, reward, done):\n",
        "    '''\n",
        "    store the experience to self.memory\n",
        "    '''\n",
        "    state = state.__array__()\n",
        "    next_state = next_state.__array__()\n",
        "    state = torch.tensor(state).to(self.device)\n",
        "    next_state = torch.tensor(next_state).to(self.device)\n",
        "    action = torch.tensor([action]).to(self.device)\n",
        "    reward = torch.tensor([reward]).to(self.device)\n",
        "    done = torch.tensor([done]).to(self.device)\n",
        "\n",
        "    self.memory.append((state, next_state, action, reward, done))\n",
        "  \n",
        "  def recall(self):\n",
        "    '''\n",
        "    Take a batch of experience from memory\n",
        "    '''\n",
        "    batch = random.sample(self.memory, self.batch_size)\n",
        "    state, next_state, action, reward, done = map(torch.stack,\n",
        "                                                  zip(*batch))\n",
        "    return state, next_state, action.squeeze(), reward.squeeze(), done.squeeze()\n",
        "  \n",
        "  def td_estimate(self, state, action):\n",
        "    # the predicted optimal Q^*{online} for a given state\n",
        "    current_Q = self.net(state, model=\"online\")[np.arange(0, self.batch_size),\n",
        "                                                action]\n",
        "    return current_Q\n",
        "  \n",
        "  @torch.no_grad()\n",
        "  def td_target(self, reward, next_state, done):\n",
        "    '''\n",
        "    TD Target - aggregation of current reward and the estimated Q\n",
        "    in the next state\n",
        "    '''\n",
        "    next_state_Q = self.net(state, model='online')\n",
        "    best_action = torch.argmax(next_state_Q, axis=1)\n",
        "    next_Q = self.net(next_state, model='target')[np.arange(0, self.batch_size),\n",
        "                                                  best_action]\n",
        "    return (reward + (1 - done.float()) * self.gamma * next_Q).float()\n",
        "  \n",
        "  def update_Q_online(self, td_estimate, td_target):\n",
        "    loss = self.loss_fn(td_estimate, td_target)\n",
        "    self.optimizer.zero_grad()\n",
        "    loss.backward()\n",
        "    self.optimizer.step()\n",
        "    return loss.item()\n",
        "  \n",
        "  def sync_Q_target(self):\n",
        "    self.net.target.load_state_dict(self.net.online.state_dict())\n",
        "  \n",
        "  def save(self):\n",
        "    num = int(self.curr_step // self.save_every)\n",
        "    save_path = (self.save_dir / f\"mario_net_{num}.chkpt\")\n",
        "    torch.save(dict(model=self.net.state_dict(),\n",
        "                    exploration_rate=self.exploration_rate), save_path)\n",
        "    print(f\"ArseNet saved to {save_path} at step {self.curr_step}\")\n",
        "  \n",
        "  def learn(self):\n",
        "    if not self.curr_step % self.sync_every:\n",
        "      self.sync_Q_target()\n",
        "    if not self.curr_step % self.save_every:\n",
        "      self.save()\n",
        "    if self.curr_step < self.burnin:\n",
        "      return None, None\n",
        "    if self.curr_step % self.learn_every:\n",
        "      return None, None\n",
        "    \n",
        "    # sample from memory\n",
        "    state, next_state, action, reward, done = self.recall()\n",
        "\n",
        "    # TD Estimate and TD Target\n",
        "    td_est = self.td_estimate(torch.tensor(state), torch.tensor(action))\n",
        "    td_trgt = self.td_target(torch.tensor(reward), torch.tensor(next_state),\n",
        "                             torch.tensor(done))\n",
        "\n",
        "    loss = self.update_Q_online(td_est, td_trgt)\n",
        "\n",
        "    return td_est.mean().item(), loss\n",
        "\n"
